package estruturasDeControle02;

//Faça um algoritmo que imprima na tela a tabuada de 1 até 10.

public class tabuada {
    static void main() {

        for (int i = 1; i <= 10; i++) {
            System.out.println();
            for (int j = 1; j <= 10; j++) {
                System.out.println(i + " x " + j + " = " + i * j);
            }

        }


    }
}
